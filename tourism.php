          <h2 class=""><strong>History</strong></h2>
          <p class="text-muted">
           Nuestra Señora de la Immaculada Conception, or more popularly known as Oslob church, is considered as one of the oldest in Cebu. It was initially established under Boljoon in 1690 but eventually became independent in 1848 with the Immaculate Conception as its patroness saint. As with other churches in Central Visayas, it was made from coral stones, which used eggs to attach one from another, as elderly locals would say.<br><br>
           The church was ruined by the fire in 2008. The incident gutted almost the entire interior of the church, including the altar and the rectory. The church's patroness saint Immaculate Conception, as well as the other effigies, were not affected as it was encased in a glass cover. <br><br>
           Between two of the oldest streets in Oslob, Calle Aeternidad and Calle Aragones, stands the Oslob Cuartel. This well-preserved structure was built in 1860 but remained unfinished at the end of the Spanish occupation in 1898. Similar to the Nuestra Señora de la Immaculada Conception, it was also made of coral stones. The cuartel is supposed to be home/barracks intended for the Spanish soldiers, more popularly known locally as guardia civil, who were guarding the shores of Oslob during the colonial era. <br><br>
           Near the bay area is the statue of Father Julian Bermejo, also known by Cebuanos as El Parroco Capitan or El Padre Capitan. He was the one who spearheaded the building of the defense system against moro invaders, which included creating a chain of watchtowers along the bays of several towns in Southern Cebu, starting from Santander all the way to Carcar.
          </p>

