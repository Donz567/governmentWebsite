          <h4 class=""><strong>History</strong></h4>
          <p class="text-muted">
            Cuartel Ruins is one of the oldest historical sites south of Cebu. The ruins are made out of the remains of a collapsed bell tower floor beside Oslob church. It is made out of 91-centimeter thick coral stones and served as a barracks for the Spanish army in the 1800s.
            <br><br>
            Cuartel means “Barracks” in the Spanish language. The construction of this historical landmark halted when the Americans came in 1899. The strong foundation of the barracks is so strong that it withstood several earthquakes and typhoons.
          </p>
